# Operatorët e krahasimit:
#  1. < : më e vogël se
#  2. > : më e madhe se
#  3. <= : më e vogël se ose baraz me
#  4. >= : më e madhe se ose baraz me 
#  5. == : i ngjashëm (ose barabartë) me
#  6. != : jo i (ose jo i barabartë) ngjashëm me

# Shprehje boolean sepse jep një vlerë boolean
x = 3 < 2
print(x)
x = 3 > 2
print(x)
x = 3 <= 2
print(x)
x = 3 >= 2
print(x)
x = "Përshëndetje" == "Përshëndetje"
print(x)
x = "Përshëndetje" != "përshëndetje"
print(x)
